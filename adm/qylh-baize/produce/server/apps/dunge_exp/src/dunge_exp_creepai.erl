-module(dunge_exp_creepai).

-include("dunge.hrl").
-include("game.hrl").
-include("scene.hrl").

%% API
-export([can_reborn/2]).

%%%-----------------------------------------------------------------------------
%%% API Functions
%%%-----------------------------------------------------------------------------
can_reborn(Actor, _SceneSt) ->
	#dunge_st{over=IsOver, wave=Wave1} = dunge_util:get_state(),
	Wave2 = maps:get(wave, Actor#actor.aiargs),
	(not IsOver) andalso (Wave1 == Wave2).

