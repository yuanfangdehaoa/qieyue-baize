-module(game_entry).

%% API
-export([open/0]).
-export([close/0]).
-export([is_opened/0]).

%%%-----------------------------------------------------------------------------
%%% API Functions
%%%-----------------------------------------------------------------------------
open() ->
	mochiglobal:put(?MODULE, true).

close() ->
	mochiglobal:put(?MODULE, false).

is_opened() ->
	true == mochiglobal:get(?MODULE).

