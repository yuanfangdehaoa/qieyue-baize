verify(GameChan, Account, Token, SDKArgs, IP) ->
	Path = case sdk_common:is_android(SDKArgs) of
		true  -> "/api/junhai/verify";
		false -> "/api/jjyou/verify"
	end,
	sdk_common:verify(Path, GameChan, Account, Token, SDKArgs, IP).

