-module(dunge_daily).

-include("dunge.hrl").

%% API
-export([send_info/2]).

%%%-----------------------------------------------------------------------------
%%% API Functions
%%%-----------------------------------------------------------------------------
send_info(_RoleID, SceneSt) ->
	#dunge_st{aiid=AIID} = dunge_util:get_state(),
	Mod = case AIID of
		210201 -> dunge_daily_wave_dungeai;
		210203 -> dunge_daily_creep_dungeai
	end,
	Mod:send_info(SceneSt).

