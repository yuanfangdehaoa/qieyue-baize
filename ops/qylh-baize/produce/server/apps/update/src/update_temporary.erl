add_robot(SceneID, RoomID, LineID, Num) ->
	Roles = [ID || #rankitem{id=ID} <- rank:get_toplist(1002, 30)],
	add_robot(SceneID, RoomID, LineID, Roles, Num).

add_robot(SceneID, RoomID, LineID, Roles, Num) ->
	lists:foreach(fun
		(_) ->
			try
				{W, H} = scene_config:size(SceneID),
				RoleID = ut_rand:choose(Roles),
				Actor  = afk_robot:init_actor(RoleID),
				Coord  = #p_coord{x=ut_rand:random(1, W*20), y=ut_rand:random(1, H*20)},
				Actor2 = Actor#actor{etime=0, born=Coord, coord=Coord, dest=Coord},
				creep:add(SceneID, RoomID, LineID, [Actor2])
			catch _:R:_ ->
				?debug("-----------------:~w", [R])
			end
	end, lists:seq(1, Num)).

